void initPST() {
    for (int sq = 0; sq < 64; sq++) {
        for (int p = 0; p < 6; p++) {
            mgTablesBlack[p][sq] = mgTablesWhite[p][mirror(sq)];
            egTablesBlack[p][sq] = egTablesWhite[p][mirror(sq)];
        }
    }
}

inline int mirror(int sq) { return sq ^ 56; } // flips rank of pieceTables